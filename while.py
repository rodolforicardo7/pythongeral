while i < 10: # enquanto o i for menor do que 10 faça:
    print(i) #imprima i
    i+= 1 # contador. Mesma coisa de i = i + 1. Então, vai rodar o programa,
    #e volta pro laço até que bata na condição imposta pelo o WHILE.
    # primeiro é 0, depois é 1 e assim por diante.